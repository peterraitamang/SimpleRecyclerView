package com.example.simplerecyclerview2.Model;

public class MyData {
    private String name;
    private int imgId;

    public MyData(String name, int imgId) {
        this.name = name;
        this.imgId = imgId;
    }

    public String getName() {
        return name;
    }

    public int getImgId() {
        return imgId;
    }
}
